		<h1 style="line-height:1.3;">Why you should use twitter for your Business</h1>

<span style="font-size:13;color:#827878;"><b> By Nitin Maheshwari</b> | 11 January 2016</span>
<br><br>

	<div class="entry-content">
		<p><img class="aligncenter" src="./Why you should use twitter for Business - QualityContent.in_files/tech-twitter-generic-2.jpg" alt="" width="100%"><br><br>
Twitter is a vehicle that allows its users to link people together who have common interests, ideas, opinions, preferences and needs. It is a way to receive and share information in real time.</p>
<p>It is used by people, and through the use of Twitter, companies have the opportunity to put the real voices of real people in their business. A way that allows a business to reveal its personality and connect on a more individual level with its customers.</p>
<h2>Twitter makes you look&nbsp;like a real person</h2>
<p>Used to its potential, Twitter bonds a business to its customers in a way that removes the official (often tight) corporate image. Using twitter conveys the truth that businesses are made up of real people.Individual customers want to know who they are doing business with.</p>
<p>They want to know that there is a real person behind the company image who cares about them. It makes a business friendly. People want to do business with someone who gives them personal attention and cares about what they care about.</p>
<h2>Traditional advertisement &nbsp;vs twitter</h2>
<p>In an advertising push, the goal may be to reassure the customer that they are cared about, and a statement may be printed in a newspaper or magazine, or voiced in a television commercial conveying "We care about you, our customer."</p>
<p>While advertising is still seen as a promise, not necessarily truth; therefore, it is likely not as effective as showing a customer they are cared about by answering their tweets, listing to what their suggestions, complaints, and compliments, and responding to them in real time.</p>
<h2>Interacting with your customers on Twitter will build trust</h2>
<p>Interacting with customers through Twitter proves to them that they are important to the business as individuals, not just a target demographic, and that proof means it is no longer just a promise, it is a proven fact.</p>
<p>Customers will say, "If someone at the business answers my question, listens to my concern, and returns my tweet personally, then I know they care about me. I will remain their customer."Using Twitter properly is a promise fulfilled to the customer, not simply a promise made.</p>
<h2>Twitter for CEO and top level&nbsp;executives</h2>
<p>Anybody who is anybody is on Twitter. If a major CEO is not on Twitter making his or her presence felt in the conversation in their business arena, then they are not connected with what is going on in business today   and they should be.</p>
<p><img class="aligncenter" src="./Why you should use twitter for Business - QualityContent.in_files/oie_SchEIDg12Dtz.jpg" alt="" width="100%"></p>
<p>A presence in this unique social media venue is advantageous as well as essential for top-level executives.<span style="line-height: 1.5;">For the first time, customers, clients, and potential customers of your product or service have the opportunity to hear the voice of the big dog.</span></p>
<p><span style="line-height: 1.5;">To deny them that voice is to deny the importance of the very people that make business possible, no matter what field of business it is   even the Pope has a Twitter account and is connecting with his followers.</span></p>
<h2>Twitter use for small business owners</h2>
<p>For small business owners it is another way to connect with your community. Successful small business owners already devote their time and knowledge pitching in to help develop community interests.</p>
<p>Your community is on Twitter, just waiting for you to join in and listen to their interests and needs . It is a mutually beneficial relationship; the community tells you what they want and need, and you can provide it to them in the form of additional products and services.</p>
<h2>Twitter Symbols, Language, and Uses</h2>
<p>Twitter has its own language, but it is easy to learn. Some call the world of Twitter the Twitterverse. Perhaps it is a play on words   somewhere between universe, and a Twitter verse   not an entire song, just a little verse to get you singing the tune.</p>
<p><img class="aligncenter" src="./Why you should use twitter for Business - QualityContent.in_files/Basics_Learn_Twitter_Tweet_example.png" alt="" width="100%" ></p>
<p>So, let's learn the language of this catchy little tune. It is important to be familiar with the simple and easy-to-understand terms, symbols, and methods of communication on Twitter before entering into a discussion on how to best use Twitter for business.</p>
<p>There are just nine main terms and concepts needed to help facilitate the discussion: tweet, retweet, favorite, mention, timeline, reply, hashtag, links, and followers.</p>
<h2>Understanding a Tweet</h2>
<p>A tweet is a message sent by the originator of the Twitter message. A tweet can be sent and received on computers, laptops, e-readers, mobile phones   anything with an internet connection. Since tweeting is real-time communication, a large percentage of tweets are received and sent on mobile devices in keeping with the idea of instant on-the-go conversations.</p>
<p><img class="aligncenter" src="./Why you should use twitter for Business - QualityContent.in_files/lbj-tweet2.jpg" alt="" width="100%"></p>
<p>Efficient in its simplicity, a typical tweet looks like this.The image above shows an original tweet by the user @King james. There are four actions that can be taken with a tweet by the person receiving it: Reply, Retweet, Mark as Favorite, and Open.</p>
<h3>More inside the Tweet</h3>
<p>This particular tweet below was sent by the user business @TheRegattabar, mentions an article on Steven feifke using the Twitter handle @Stevenfeifke, and includes a link to the article referring to One of the article. The tweeted link bit.ly/1qvyp19&nbsp;can be clicked to go to the article containing the news.</p>
<p>#jazz #harvardsuare #livemusic&nbsp;(notice the leading hashtag character before the name) identifies a topic of conversation that interested Twitter users can follow to view and participate in conversations about the topic that the hashtag plus name identifies.</p>

<p>This tweet is an excellent example of how to get out a great deal of information within the 140 character limit through its conversation hashtag topic, its use of the @Stevenfeifke Twitter quote, link to that article, and original message.</p>
<h2>Understanding RETWEET</h2>
<p>A retweet is a message passed on to all of your followers by the retweet button on the tweet. Recall the options available again when someone receives a tweet: Reply, Retweet, Mark as Favorite, and Open.</p>

<p>In this particular case, Jeff Bullas, Twitter handle @Jeffbullas, tweeted a compliment about his experience with&nbsp;digital marketer Jonathan&nbsp;and as a recipient of the tweet and good publicity; Jonathan&nbsp;wanted to spread the word to its followers that Jeff&nbsp;was a happy customer. So Jonathan re-tweeted Jeff Bulla's tweet.</p>
<h3>More inside the Retweet</h3>
<p>In order to re-tweet any Tweet you need to click&nbsp;the option re-tweet as highlighted in the image below, and your&nbsp;tweet will be&nbsp;passed on in the form of a retweet to all of the your&nbsp;Twitter followers. This is a great use of wordof- mouth, or rather, word-of-tweet advertising from a real customer in real time.</p>
<p><img class="aligncenter" src="./Why you should use twitter for Business - QualityContent.in_files/retweet.jpg" alt="" width="100%" ></p>
<p>A business can tweet a great offer, coupon, or breaking business news, to its followers, and those followers can retweet that information to their followers, making the original tweet spread to hundreds, thousands, even millions of people worldwide by sending one simple 140 character tweet.&nbsp;Wow! This is powerful marketing and highly leveraged.</p>

